    "ESB_TRANSACTION_ID"   VARCHAR2(40),
    "ESB_RECV_DT"          VARCHAR2(20),
    "ESB_INTERFACE_ID"     VARCHAR2(40),
    "VARIABLE"             VARCHAR2(200),
    "BREAKDOWN"            VARCHAR2(30),
    "BREAKDOWN_CATEGORY"   VARCHAR2(60),
    "YYEAR"                VARCHAR2(4),
